package Models;

import java.math.BigDecimal;

public class Vol_habilidad {

    private BigDecimal id;
    private BigDecimal id_vol;
    private BigDecimal id_hab;

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public BigDecimal getId_vol() {
        return id_vol;
    }

    public void setId_vol(BigDecimal id_vol) {
        this.id_vol = id_vol;
    }

    public BigDecimal getId_hab() {
        return id_hab;
    }

    public void setId_hab(BigDecimal id_hab) {
        this.id_hab = id_hab;
    }
}
